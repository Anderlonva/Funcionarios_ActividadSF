    private void tabla_funcionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_funcionariosMouseClicked
        if(tabla_funcionarios.getSelectedRow() != -1){
            btn_eliminar.setEnabled(true);
            btn_actualizar.setEnabled(true);
            btn_agregar.setEnabled(false);
            
            int fila = tabla_funcionarios.getSelectedRow();
            
            txt_documento.setText(tabla_funcionarios.getValueAt(fila, 0).toString());
            txt_nombre.setText(tabla_funcionarios.getValueAt(fila, 1).toString());
            txt_apellidos.setText(tabla_funcionarios.getValueAt(fila, 2).toString());
            txt_sexo.setText(tabla_funcionarios.getValueAt(fila, 3).toString());
            txt_direccion.setText(tabla_funcionarios.getValueAt(fila, 4).toString());
            txt_telefono.setText(tabla_funcionarios.getValueAt(fila, 5).toString());
            dchooser_fechaNacimiento.setToolTipText(tabla_funcionarios.getValueAt(fila, 6).toString());
        }
    }//GEN-LAST:event_tabla_funcionariosMouseClicked

