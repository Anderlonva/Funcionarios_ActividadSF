    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed

        Funcionario funcionario = new Funcionario();
        TipoIdentificacion tipoIdentificacion = new TipoIdentificacion(cbox_tipoId.getSelectedIndex() + 1);
        EstadoCivil estadoCivil = new EstadoCivil(cbox_estadoCivil.getSelectedIndex() + 1);

        funcionario.setTipoIdentificacion(tipoIdentificacion);
        funcionario.setDocumento(Integer.parseInt(txt_documento.getText().trim()));
        funcionario.setNombre(txt_nombre.getText());
        funcionario.setApellido(txt_apellidos.getText());
        funcionario.setEstadoCivil(estadoCivil);
        funcionario.setSexo(txt_sexo.getText());
        funcionario.setDireccion(txt_direccion.getText());
        funcionario.setTelefono(txt_telefono.getText());
        if (dchooser_fechaNacimiento.getDate() != null) {

            funcionario.setFechaNacimiento(dchooser_fechaNacimiento.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        } else {
            funcionario.setFechaNacimiento(LocalDate.now(ZoneId.systemDefault()));
        }

        controladorFuncionarios.guardarFuncionario(funcionario);

        llenarTablaFuncionarios();
        desactivarCajas();
        limpiarTXT();
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        int fila = tabla_funcionarios.getSelectedRow(); //formulario.tblDatos.getSelectedRow();
        if (fila == -1 ) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Funcionario o ingresar el Documento");
        } else {
            btn_eliminar.setEnabled(true);

            if (Mensajes.mensajeConfirmacion("Eliminar Usuario", "Esta seguro en eliminar el usuario")) {
               
                int  id_doc = Integer.parseInt((String) tabla_funcionarios.getValueAt(fila, 0).toString());
                    controladorFuncionarios.borrar(id_doc);

            }
        }

        llenarTablaFuncionarios();
        activarCajas();
        desactivarCajas();
        limpiarTXT();
    }//GEN-LAST:event_btn_eliminarActionPerformed

